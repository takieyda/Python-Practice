list = ["billowy", "biopsy", "chinos", "defaced", "chintz", "sponged",\
        "bijoux", "abhors", "fiddle", "begins", "chimps", "wronged"]

for item in list:
    if item == list.sort():
        print(item, "IN ORDER")
    else:
        print(item, "NOT IN ORDER")
